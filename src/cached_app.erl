-module('cached_app').

-behaviour(application).

-include("cached.hrl").

%% Application callbacks
-export([start/2
        ,stop/1]).

%%====================================================================
%% API
%%====================================================================

start(_StartType, _StartArgs) ->
	ets:new(?TABLE, [
		named_table,
		public,
		{write_concurrency, true},
		{read_concurrency, true}
	]),
    'cached_sup':start_link().

%%--------------------------------------------------------------------
stop(_State) ->
    ok.

